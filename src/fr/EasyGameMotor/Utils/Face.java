package fr.EasyGameMotor.Utils;

public class Face {
	
	int A;
	int B;
	int C;
	int D;
	int E;
	int F;
	int G;
	int H;
	int I;
	
	public Face(int a, int b, int c, int d, int e, int f, int g, int h, int i) {
		
		A = a;
		B = b;
		C = c;
		D = d;
		E = e;
		F = f;
		G = g;
		H = h;
		I = i;
	}
	
	public int getA() {
		
		return A;
	}

	public int getB() {
		
		return B;
	}

	public int getC() {
		
		return C;
	}

	public int getD() {
		
		return D;
	}

	public int getE() {
		
		return E;
	}

	public int getF() {
		
		return F;
	}

	public int getG() {
		
		return G;
	}

	public int getH() {
		
		return H;
	}

	public int getI() {
		
		return I;
	}
	
	public void setA(int a) {
		
		A = a;
	}
	
	public void setB(int b) {
		
		B = b;
	}
	
	public void setC(int c) {
		
		C = c;
	}
	
	public void setD(int d) {
		
		D = d;
	}
	
	public void setE(int e) {
		
		E = e;
	}
	
	public void setF(int f) {
		
		F = f;
	}
	
	public void setG(int g) {
		
		G = g;
	}
	
	public void setH(int h) {
		
		H = h;
	}
}
